function observeReveals() {
  const revealElements = document.querySelectorAll(".reveal");
  const observer = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        if (entry.isIntersecting) {
          entry.target.classList.add("is-visible");
          observer.unobserve(entry.target);
        }
      });
    },
    {
      threshold: 0.12,
      rootMargin: "0px 0px -6% 0px",
    },
  );

  revealElements.forEach((element) => observer.observe(element));
}

let ticking = false;

function updateParallax() {
  if (!portrait) {
    return;
  }

  const offset = Math.min(window.scrollY * 0.05, 22);
  portrait.style.setProperty("--parallax", `${offset}px`);
  ticking = false;
}

window.addEventListener(
  "scroll",
  () => {
    if (!ticking) {
      window.requestAnimationFrame(updateParallax);
      ticking = true;
    }
  },
  { passive: true },
);

langButtons.forEach((button) => {
  button.addEventListener("click", () => {
    const nextLang = button.dataset.setLang;
    if (!nextLang || nextLang === html.dataset.lang) {
      return;
    }

    body.classList.add("is-switching");
    window.setTimeout(() => {
      render(nextLang);
      body.classList.remove("is-switching");
    }, 120);
  });
});

render(localStorage.getItem("site-lang") || "zh");
updateParallax();
